# Creating a dictionary to store information about a person
person = {
    "first_name": "abiha",
    "last_name": "asad",
    "age": 19,
    "city": "pakistan"
}

# Printing each piece of information
print("First Name:", person["first_name"])
print("Last Name:", person["last_name"])
print("Age:", person["age"])
print("City:", person["city"]) 
