# For loop 1: in increments of 1 counts upwords from 0 to 50
print("in increments of 1 counting upwords from 0 to 50 :")
for i in range(0, 51, 1):
    print(i)

# For loop 2: in decrements of 1 counts down from 50 to 0
print("\nin decrements of 1 counting down from 50 to 0 :")
for i in range(50, -1, -1):
    print(i)

# For loop 3: in increments of 1 counts up from 30 to 50
print("\nn increments of 1 counting up from 30 to 50 i:")
for i in range(30, 51, 1):
    print(i)

# For loop 4: in decrements of 2 counts down from 50 to 10
print("\nin decrements of 2 counting down from 50 to 10 :")
for i in range(50, 9, -2):
    print(i)

# For loop 5: in increments of 5 counts up from 100 to 200
print("\nin increments of 5 counting up from 100 to 200 :")
for i in range(100, 201, 5):
    print(i)
